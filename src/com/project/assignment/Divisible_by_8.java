package com.project.assignment;

public class Divisible_by_8
{
    public int div_by_8(int a)
    {
        if (a % 8 == 0)
        {
            System.out.println("This num is div by 8 !\n");
        }
        else
        {
            System.out.println("This num is not div by 8 !\n");
        }
        return a;
    }
}
